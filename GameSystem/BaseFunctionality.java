package GameSystem;

public abstract class BaseFunctionality implements BaseFunctionalityInterface {
    private int gameId, score;

    public int getGameId() {
        return gameId;
    }

    public void setGameId(int gameId) {
        this.gameId = gameId;
    }

    public int getScore() {
        return score;
    }

    public void setScore(int score) {
        this.score = score;
    }
}
